    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "friendCell", for: indexPath)
        //as? UserCell else { return UITableViewCell()}
        let userID = FirebaseSystem.shared.friendList[indexPath.row]
        
        let query = FirebaseSystem.shared.usersReference.whereField("uid", isEqualTo: userID)
        print(query.getDocuments(completion: { (snapshot, err) in
            if let err = err {
                print(err)
            }
            else {}
        }))
        cell.textLabel?.text = userID
        return cell
    }
    
